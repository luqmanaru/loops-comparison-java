package com.mycompany.cetakseratuskalidowhile;

/**
 * Demonstrasi Do-While Loop 
 * @author luqmanaru
 */
public class CetakSeratusKaliDoWhile {
    public static void main(String[] args) {
        int count = 0;
        
        do {
            System.out.println("Nama saya");
            count++;
        } while (count < 100);
    }
}
